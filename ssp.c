#include "push_swap.h"

void	sa(t_stack **a)
{
	ft_swap(&(*a)->arr[0], &(*a)->arr[1]);
}

void	sb(t_stack **b)
{
	sa(b);
}

void	ss(t_stack **a, t_stack **b)
{
	sa(a);
	sb(b);
}
