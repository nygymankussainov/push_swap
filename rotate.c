#include "push_swap.h"

void	ra(int **a)
{
	int	i;

	i = 1;
	while ((*a)[i])
	{
		ft_swap(&(*a)[i - 1], &(*a)[i]);
		i++;
	}
}

void	rb(int **b)
{
	ra(b);
}

void	rr(int **a, int **b)
{
	ra(a);
	rb(b);
}
